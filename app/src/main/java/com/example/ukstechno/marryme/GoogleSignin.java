    private void loginViaGoogle() {
        Utils.show(this);
        StringRequest jsObjRequest = new StringRequest(Request.Method.POST,
                "",
                new Response.Listener<String>() {
                    @Override
                    public void onResponse(String response) {
                        Log.d("Response ", response);
                        Utils.dismiss();
                        try {
                            JSONObject jsonObject = new JSONObject(response);
                            if (jsonObject.has("status")) {
                                try {
                                    if (jsonObject.getString("status").equalsIgnoreCase("success")) {
                                        // saveToLocally();
                                        Utils.setUserCredential(GoogleSignin.this, jsonObject.getString("user_id"),
                                                jsonObject.getString("email"), jsonObject.getString("user_name"), true);
                                        startActivity(new Intent(getApplicationContext(), UpdateProfile.class));
                                        Toast.makeText(GoogleSignin.this, "Successfully logged in.", Toast.LENGTH_SHORT).show();
                                    } else if (jsonObject.getString("status").equalsIgnoreCase("complete")){
                                        Intent intent=new Intent(getApplicationContext(),MatchingProfile.class);
                                        startActivity(intent);
                                    }
                                    else {
                                        Toast.makeText(GoogleSignin.this, "username or password is incorrect",
                                                Toast.LENGTH_SHORT).show();
                                    }
                                } catch (JSONException e) {
                                    e.printStackTrace();
                                }
                            }
                        } catch (JSONException e) {
                            e.printStackTrace();
                        }

                    }
                }, new Response.ErrorListener() {

            @Override
            public void onErrorResponse(VolleyError error) {
                Log.d("Error: ", error.toString());
                Utils.dismiss();
            }

        }) {
            @Override
            protected Map<String, String> getParams() throws AuthFailureError {
                Map<String, String> params = new HashMap<>();
                params.put("user_type","google");
                params.put("username", acct.getDisplayName());
                params.put("email", acct.getEmail());
                return params;
            }
        };
        RequestQueue queue = Volley.newRequestQueue(this);
        queue.add(jsObjRequest);

    }
